    out = model.generate(**context_inputs, **generation_kwargs)
    text = tokenizer.decode(out[0], skip_special_tokens=True)
    # strip the context from decoded (keep the generated tail)
    decoded_after_context = text[len(tokenizer.decode(context_inputs["input_ids"][0], skip_special_tokens=True)):].strip()
    # split into sentences and return first sentence
    sents = sent_tokenize(decoded_after_context)
    return sents[0] if len(sents) > 0 else "" # Return empty string if no sentences are found

