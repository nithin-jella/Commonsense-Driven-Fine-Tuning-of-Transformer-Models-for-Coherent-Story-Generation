def tokenize_function(example):
    # Encode context (first 4 sentences)
    context_encodings = tokenizer(
        example["story_context"], truncation=True
    )

    # Encode ending (5th sentence)
    ending_encodings = tokenizer(
        example["story_ending"], truncation=True
    )

    # Concatenate context + ending for GPT-2 causal training
    input_ids = context_encodings["input_ids"] + ending_encodings["input_ids"]
    attention_mask = context_encodings["attention_mask"] + ending_encodings["attention_mask"]
    labels = input_ids.copy()  # labels = next-token prediction

    # Pad the combined sequence to a fixed maximum length
    max_length = 128 + 32  # Sum of individual max lengths
    padding_length = max_length - len(input_ids)

    if padding_length > 0:
        input_ids = input_ids + [tokenizer.pad_token_id] * padding_length
        attention_mask = attention_mask + [0] * padding_length
        labels = labels + [-100] * padding_length # Use -100 to ignore padding in loss calculation

    return {"input_ids": input_ids, "attention_mask": attention_mask, "labels": labels}

