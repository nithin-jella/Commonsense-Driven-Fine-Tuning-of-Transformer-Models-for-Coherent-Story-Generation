import re

def preprocess_data(example):
    # Split story into sentences
    sentences = re.split(r'(?<=[.!?]) +', example['text'].strip())

    if len(sentences) >= 5:
        context = " ".join(sentences[:4])  # First 4 sentences
        ending = sentences[4]              # 5th sentence as target
    else:
        # If story has less than 5 sentences
        context = " ".join(sentences[:-1])
        ending = sentences[-1] if sentences else ""

    return {"story_context": context, "story_ending": ending}

